def get_variety_options(variety_name):
    """מחזיר רשימת וריאציות של זן"""
    if variety_name in FRUIT_SIZE_DB:
        keys = list(FRUIT_SIZE_DB[variety_name].keys())
        if keys == ['_default']:
            return None
        return keys
    return None

def get_harvest_months(variety_name, sub_variety=None):
    """מחזיר רשימת חודשי קטיף אפשריים"""
    if variety_name not in FRUIT_SIZE_DB:
        return None
    data = FRUIT_SIZE_DB[variety_name]
    if sub_variety and sub_variety in data:
        return list(data[sub_variety].keys())
    if '_default' in data:
        return list(data['_default'].keys())
    return None

def find_closest_dates(variety, sub_variety, harvest_month, today_month, today_day):
    """מוצא את שני התאריכים הקרובים (לפני ואחרי) בטבלה"""
    if variety not in FRUIT_SIZE_DB:
        return None
    data = FRUIT_SIZE_DB[variety]
    if sub_variety and sub_variety in data:
        rows = data[sub_variety].get(harvest_month)
    elif '_default' in data:
        rows = data['_default'].get(harvest_month)
    else:
        return None
    
    if not rows:
        return None
    
    # המרת תאריך נוכחי למספר השוואה (חודש*100 + יום)
    today_val = today_month * 100 + today_day
    
    # התאמה ל"שנה חקלאית" - אם אנחנו אחרי חודש 5, חודשים 1-5 נחשבים שנה הבאה
    def date_val(m, d):
        v = m * 100 + d
        # אם אנחנו במחצית השנייה ויש תאריך ינואר-מאי, זה השנה הבאה
        if today_month >= 6 and m <= 5:
            v += 1200
        # אם אנחנו במחצית הראשונה ויש תאריך יוני-דצמבר, זה השנה הקודמת
        elif today_month <= 5 and m >= 6:
            v -= 1200
        return v
    
    today_compare = today_month * 100 + today_day
    if today_month <= 5:
        today_compare += 0  # השנה הנוכחית
    
    before = None
    after = None
    
    for (m, d), mn, mx in rows:
        v = date_val(m, d)
        if v <= today_compare:
            if before is None or v > date_val(before[0][0], before[0][1]):
                before = ((m, d), mn, mx)
        if v >= today_compare:
            if after is None or v < date_val(after[0][0], after[0][1]):
                after = ((m, d), mn, mx)
    
    return {'before': before, 'after': after}
