def get_pest_treatments(pest_name):
    """קבל את כל התכשירים המורשים למזיק מסוים"""
    pest_key = pest_name.replace(" ", "_")
    for key in PESTS_AND_TREATMENTS:
        if pest_name.lower() in key.lower() or pest_name in PESTS_AND_TREATMENTS[key]["שם_עברי"]:
            return PESTS_AND_TREATMENTS[key]
    return None

def search_treatment(search_term):
    """חפש תכשיר לפי שם"""
    results = []
    for pest_key, pest_data in PESTS_AND_TREATMENTS.items():
        for treatment in pest_data["תכשירים"]:
            if search_term.lower() in treatment["שם"].lower():
                results.append({
                    "מזיק": pest_data["שם_עברי"],
                    "תכשיר": treatment
                })
    return results

def list_all_pests():
    """הצג רשימה של כל המזיקים"""
    return [{"שם": v["שם_עברי"], "מדעי": v["שם_מדעי"]} for v in PESTS_AND_TREATMENTS.values()]

def build_pests_context():
    """בנה טקסט מלא של כל המזיקים והתכשירים לcontext של Claude"""
    lines = []
    for key, pest in PESTS_AND_TREATMENTS.items():
        lines.append(f"\nמזיק: {pest['שם_עברי']} ({pest['שם_מדעי']})")
        lines.append("תכשירים מורשים:")
        for t in pest["תכשירים"]:
            line = f"  - {t['שם']}: חומר {t['חומר']}, ריכוז {t['ריכוז']}, ימי המתנה {t['ימי_המתנה']}"
            if t.get("הערות"):
                line += f" | הערה: {t['הערות']}"
            lines.append(line)
    return "\n".join(lines)

